function Switches(val){
    var answer = "";

switch(val){
    case "bob":
        answer = "Marley";
        break;
    case 42:
        answer = "The Answer";
        break;
    case 1:
        answer = "There is no 1"
        break;
    case 99:
        answer = "Missed me by this so much"
        break;
    case 7:
        answer = ""
}

console.log(answer);
}

Switches(1)
